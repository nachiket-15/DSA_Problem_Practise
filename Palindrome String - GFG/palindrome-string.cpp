//User function template for C++
class Solution{
public:	
	
	
	int isPalindrome(string S)
	{
	    stack<char>st;
	    for(auto i:S){
	        st.push(i);
	    }
	    string s2="";
	    while(!st.empty()){
	        s2+=st.top();
	        st.pop();
	    }
	    return s2==S;
	}

};

