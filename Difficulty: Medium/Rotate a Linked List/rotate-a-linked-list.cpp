class Solution
{
    public:
    //Function to rotate a linked list.
    Node* rotate(Node* head, int k)
    {
        if(head==NULL || head->next==NULL || k==0)
        {
            return head;
        }
        
        Node*ptr=head;
        Node*prev=head;
        int len=0;
        
        while(ptr){
            len++;
            prev=ptr;
            ptr=ptr->next;
        }
        
        k=k%len;
        prev->next=head;
        
        
        while(k--){
            prev=prev->next;   
        }
        
        head=prev->next;
        prev->next=NULL;
        return head;
        
    }
};
    




